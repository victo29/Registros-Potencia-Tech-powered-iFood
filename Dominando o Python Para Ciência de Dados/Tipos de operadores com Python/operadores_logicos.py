saldo = 1000
saque = 200
limite =100

print(saldo >= limite and saque <= limite) # Operador E/&& as duas expressões devem ser verdadeira para retornar um valor True

print(saldo >= limite or saque <= limite) # Operador OU/|| apenas uma expressão precisa ser verdadeira para retornar True

print(not 1000 > 1500) # Operador not, ele é a negação e sempre vai exibir o resultado contrario

print(not "250") # Strings com conteudo retornam um valor True mas com o not elas retornam um valor False

print(not "") # Strings sem conteudo retornam um valor False mas com o not elas retornam um valor True



